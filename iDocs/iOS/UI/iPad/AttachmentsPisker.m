#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    [self.tableView deselectRowAtIndexPath:indexPath animated:YES];
    
    ReportAttachment *selectedReport = (ReportAttachment*)[self.attachmentsArray objectAtIndex:indexPath.row];
    
    if([delegate respondsToSelector:@selector(reportAttachmentFileSelected:)])
    {
        [delegate reportAttachmentFileSelected:selectedReport];
    }
}

